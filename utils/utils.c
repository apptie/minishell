#include "minishell.h"

void	skip_space(char *str, int *i)
{
	while (str[*i] == ' '
		|| str[*i] == '\t'
		|| str[*i] == '\r'
		|| str[*i] == '\v'
		|| str[*i] == '\f')
		(*i)++;
}
